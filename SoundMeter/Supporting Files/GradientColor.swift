    func gravePreserveGroundbreakingOccasion(topColor:UIColor, bottomColor:UIColor) {
        let throwZoomCompanionable = CAGradientLayer()
        throwZoomCompanionable.frame = self.bounds
        throwZoomCompanionable.colors = [
            topColor.cgColor,
            bottomColor.cgColor
        ]
